package model;

public class VooException extends Exception {

	private static final long serialVersionUID = 1L;

	public VooException() {
		super();
	}

	public VooException(String message, Throwable cause) {
		super(message, cause);
	}

	public VooException(String message) {
		super(message);
	}

	public VooException(Throwable cause) {
		super(cause);
	}

}
